import mongoose from "mongoose";

const AirConditionerSchema = new mongoose.Schema({
  _id: mongoose.Schema.Types.ObjectId,
  name: String,
  Title: String,
  Brand: String,
  ModelNo: String,
  ACType: String,
  CapacityInTons: String,
  StarRating: String,
  InverterTechnology: String,
  IndoorNoiseLevel: String,
  OperatingCurrent: String,
  CoolingCapacity: String,
  PowerInput: String,
  EER: String,
  PowerRequirements: String,
  Dehumidification: String,
  OperatingMode: String,
  OtherModes: String,
  Remote: String,
  AntiBacteriaFilter: String,
  OtherFilters: String,
  AutoRestart: String,
  AutoClean: String,
  EnergySave: String,
  FrontPanelDisplay: String,
  HotAndCold: String,
  SelfDiagnosis: String,
  Timer: String,
  Color: String,
  Compressor: String,
  EvaporatorFinType: String,
  MainUnitCondenserCoil: String,
  OutdoorUnitCondenserCoil: String,
  Refrigerant: String,
  AirCirculationIn: String,
  AirCirculationOut: String,
  AirFlowDirection: String,
  AutoAirSwing: String,
  FanSpeedSteps: String,
  SelectableSwing: String,
  SpeedSetting: String,
  InstallationWarranty: String,
  variants: [
    {
      store: String,
      price: String,
      store_link: String,
    },
  ],
  images: [String],
  "Expert Score": String,
});

export const AirConditioners = mongoose.model("acs", AirConditionerSchema);
